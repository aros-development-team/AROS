static void p2c(UBYTE *source, UBYTE *dest, LONG width, LONG height,
    	    	LONG totplanes, LONG wantplanes, LONG planarbpr, LONG chunkybpr)
{
    LONG alignedwidth, x, y, p, bpl;
    
    alignedwidth = (width + 15) & ~15;
    bpl = planarbpr * totplanes;
    
    for(y = 0; y < height; y++)
    {
    	for(x = 0; x < width; x++)
	{
	    LONG  mask   = 1 << (7 - (x & 7));
	    LONG  offset = x / 8;
	    UBYTE chunkypix = 0;
	    
	    for(p = 0; p < wantplanes; p++)
	    {
	    	if (source[p * planarbpr + offset] & mask) chunkypix |= (1 << p);
	    }
	    dest[x] = chunkypix;
	}
	
	source += bpl;
	dest += chunkybpr;
    }
    
    
}

/****************************************************************************************/

static void makechunky(void)
{
    WORD y;
    
    chunkydata = AllocVec(PATTERN_WIDTH * PATTERN_HEIGHT, MEMF_PUBLIC);
    if (!chunkydata) cleanup("Out of memory!");
    
    for(y = 0; y < PATTERN_HEIGHT; y++)
    {
    	p2c(&patterndata[y * PATTERN_WIDTH / 8],
	    &chunkydata[y * PATTERN_WIDTH],
	    PATTERN_WIDTH,
	    1,
	    PATTERN_DEPTH,
	    PATTERN_DEPTH,
	    PATTERN_PLANESIZE,
	    PATTERN_WIDTH);
    }
    
}

