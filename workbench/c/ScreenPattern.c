static void cleanup(char *msg)
{
    if (msg) printf("screenpattern: %s\n", msg);
    
    if (pens_alloced)
    {
    	WORD i;
	
	for(i = 0; i < (1 << PATTERN_DEPTH); i++)
	{
	    if (remaptable[i] != -1) ReleasePen(cm, remaptable[i]);
	}
    }

    if (patternbm)
    {
    	WaitBlit();
	FreeBitMap(patternbm);
    }

    if (scr) UnlockPubScreen(NULL, scr);

    if (chunkydata) FreeVec(chunkydata);

    exit(0);
}

