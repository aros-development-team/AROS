#include <grub/mm.h>
#include <grub/misc.h>
#include <grub/kernel.h>
#include <grub/xen.h>

void
grub_halt (void)
{
  struct sched_shutdown arg;

  arg.reason = SHUTDOWN_poweroff;
  grub_xen_sched_op (SCHEDOP_shutdown, &arg);
  for (;;);
}
